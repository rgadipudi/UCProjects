    @SuppressWarnings("unchecked")
	void listPorts()
    {
		java.util.Enumeration<CommPortIdentifier> portEnum = CommPortIdentifier.getPortIdentifiers();
		jTextArea1.setForeground(Color.BLACK);
        jTextArea1.append("Available Ports: \n");
        while ( portEnum.hasMoreElements() ) 
        {
        	// Identifies the available port number and its type
            CommPortIdentifier portIdentifier = portEnum.nextElement();
           jTextArea1.append(portIdentifier.getName()  +  " - " +  getPortTypeName(portIdentifier.getPortType())+"\n" );
        }      
        jTextArea1.setForeground(Color.BLACK);
        jTextArea1.append("Enter COMM port number and press connect \n");
    }
	String getPortTypeName ( int portType )
    {
        switch ( portType )
        {
            case CommPortIdentifier.PORT_I2C:
                return "I2C";
            case CommPortIdentifier.PORT_PARALLEL:
                return "Parallel";
            case CommPortIdentifier.PORT_RAW:
                return "Raw";
            case CommPortIdentifier.PORT_RS485:
                return "RS485";
            // We need Serial Port
            case CommPortIdentifier.PORT_SERIAL:
                return "Serial";
            default:
                return "unknown type";
        }
    }
    
